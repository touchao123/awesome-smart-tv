Patterns and Practices
This is the main repository for the community driven SharePoint / Office Dev Patterns and Practices (PnP) initiative. If you are looking for latest news around PnP or related topics, please have a look on our one pager at http://aka.ms/sppnp.

SharePoint Patterns and Practices logo

PnP initiative has numerous GitHub repositories, so that you can more easily find what's relevant for you depending on your interest. Easiest way to follow up on latest changes is our landing page at http://aka.ms/OfficeDevPnP. We also recommend you to use the PnP sample and solution search tool at dev.office.com for finding relevant content for you.

